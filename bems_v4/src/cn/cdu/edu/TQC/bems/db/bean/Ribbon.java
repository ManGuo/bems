public class Ribbon {
	
	private int ribbonId;
	private int ribbonTypeId;
	private String location;
	private String size;
	private int floorId;
	
	public Ribbon(){
		
	}
	
	public Ribbon(int ribbonId,
			int ribbonTypeId,
			String location,
			String size,
			int floorId){
		this.ribbonId = ribbonId;
		this.ribbonTypeId = ribbonTypeId;
		this.location = location;
		this.size = size;
		this.floorId = floorId;
	}
	
	
	public int getRibbonId() {
		return ribbonId;
	}
	public void setRibbonId(int ribbonId) {
		this.ribbonId = ribbonId;
	}
	public int getRibbonTypeId() {
		return ribbonTypeId;
	}
	public void setRibbonTypeId(int ribbonTypeId) {
		this.ribbonTypeId = ribbonTypeId;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public int getFloorId() {
		return floorId;
	}
	public void setFloorId(int floorId) {
		this.floorId = floorId;
	}
	
	

}
