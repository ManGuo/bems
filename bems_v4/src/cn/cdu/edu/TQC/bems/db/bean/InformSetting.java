public class InformSetting {
	
	private int informSettingId;
	private String emailUser;
	private String emailPassword;
	private String messagePort;
	private String messagePassword;
	
	public InformSetting(){
		
	}
	
	public InformSetting(int informSettingId,
			String emailUser,
			String emailPassword,
			String messagePort,
			String messagePassword){
		this.informSettingId = informSettingId;
		this.emailUser = emailUser;
		this.emailPassword = emailPassword;
		this.messagePort = messagePort;
		this.messagePassword = messagePassword;
		
	}
	
	
	public int getInformSettingId() {
		return informSettingId;
	}
	public void setInformSettingId(int informSettingId) {
		this.informSettingId = informSettingId;
	}
	public String getEmailUser() {
		return emailUser;
	}
	public void setEmailUser(String emailUser) {
		this.emailUser = emailUser;
	}
	public String getEmailPassword() {
		return emailPassword;
	}
	public void setEmailPassword(String emailPassword) {
		this.emailPassword = emailPassword;
	}
	public String getMessagePort() {
		return messagePort;
	}
	public void setMessagePort(String messagePort) {
		this.messagePort = messagePort;
	}
	public String getMessagePassword() {
		return messagePassword;
	}
	public void setMessagePassword(String messagePassword) {
		this.messagePassword = messagePassword;
	}
	
	
	
	

}
